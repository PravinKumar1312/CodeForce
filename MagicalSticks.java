import java.util.*;

public class MagicalSticks {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while (t-- > 0) {
            long n = sc.nextLong();
            long m = (n + 1) / 2;
            ;
            System.out.println(m);
        }

        sc.close();
    }
}
